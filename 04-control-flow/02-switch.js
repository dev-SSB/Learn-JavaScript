const val1 = "SSB";

switch(val1){
    case "PP":
        console.log("PP");
        break;
    case "Ayu":
        console.log("Ayu");
        break;
    case "SSB":
        console.log("SSB");
        break;
    default:
        console.log("Default");
        break;       
}





const val2 = 100;

switch(val2){
    case 100:
        console.log(100);
        break;
    case 200:
        console.log(200);
        break;
    case 300:
        console.log(300);
        break;
    default:
        console.log("Default");
        break;       
}





